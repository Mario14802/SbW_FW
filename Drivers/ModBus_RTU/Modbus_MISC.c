//decoding functions

//to be mostly used with write coils
bool MB_Parse_Bit(uint8_t *data, uint16_t Address)
{
	return (data[Address / 8] & BitMasks[Address % 8]) ? 1 : 0;
}
void MB_Parse_Coils(uint8_t *data, uint16_t Offset, uint8_t *Output, uint16_t Length)
{
	for (uint8_t x = 0; x < Length; x++)
	{
		uint16_t idx = Offset + x;
		//erase the designated area
		Output[idx / 8] &= NbitMasks[idx % 8];
		//write the designated area
		Output[idx / 8] |= (data[x / 8] & BitMasks[x % 8]) ? BitMasks[idx % 8] : 0;
	}
}
void MB_Parse_UInt8(uint8_t *data, uint16_t *offset, uint8_t *Output)
{
	*Output = data[(*offset)];
	*offset += 1;
}
void MB_Parse_Int8(uint8_t *data, uint16_t *offset, int8_t *Output)
{
	*Output = data[(*offset)];
	*offset += 1;
}
void MB_Parse_UInt16(uint8_t *data, uint16_t *offset, uint16_t *Output)
{
	*Output = data[(*offset)] << 8 | (uint16_t) data[(*offset) + 1];
	*offset += 2;
}
void MB_Parse_Int16(uint8_t *data, uint16_t *offset, int16_t *Output)
{
	*Output = data[(*offset)] << 8 | (uint16_t) data[(*offset) + 1];
	*offset += 2;
}
void MB_Parse_Float(uint8_t *data, uint16_t *offset, float *Output)
{
	*Output = data[(*offset)] << 24 | data[(*offset) + 1] << 16 | data[(*offset) + 2] << 8 | (uint16_t) data[(*offset) + 3];
	*offset += 4;
}

//encoding functions
void MB_Encode_Bit(uint8_t *data, uint16_t Address, bool bit)
{
	if (bit)
	{
		data[Address / 8] |= BitMasks[Address % 8];
	}
	else
	{
		data[Address / 8] &= NbitMasks[Address % 8];
	}
}
void MB_Encode_Coils(uint8_t *data, uint16_t Offset, uint8_t *Output, uint16_t Length)
{

	//erase the buffer area
	memset(Output, 0, (Length / 8) + ((Length % 8) ? 1 : 0));

	for (uint8_t x = 0; x < Length; x++)
	{
		uint16_t idx = Offset + x;
		//write the designated area
		Output[x / 8] |= (data[idx / 8] & BitMasks[idx % 8]) ? BitMasks[x % 8] : 0;
	}
}
void MB_Encode_UInt8(uint8_t *data, uint8_t Input, uint16_t *Offset)
{
	data[(*Offset)++] = (uint8_t) (Input);
}
void MB_Encode_Int8(uint8_t *data, int8_t Input, uint16_t *Offset)
{
	data[(*Offset)++] = (uint8_t) (Input);
}
void MB_Encode_UInt16(uint8_t *data, uint16_t Input, uint16_t *Offset)
{
	data[(*Offset)++] = (uint8_t) (Input >> 8);
	data[(*Offset)++] = (uint8_t) (Input);
}
void MB_Encode_Int16(uint8_t *data, int16_t Input, uint16_t *Offset)
{
	data[(*Offset)++] = (uint8_t) (Input >> 8);
	data[(*Offset)++] = (uint8_t) (Input);
}
void MB_Encode_Float(uint8_t *data, float Input, uint16_t *Offset)
{
	data[(*Offset)++] = (uint8_t) ((uint32_t) Input >> 24);
	data[(*Offset)++] = (uint8_t) ((uint32_t) Input >> 16);
	data[(*Offset)++] = (uint8_t) ((uint32_t) Input >> 8);
	data[(*Offset)++] = (uint8_t) ((uint32_t) Input);
}

